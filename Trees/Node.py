class Node:
    
    data = -1
    leftChild = None
    rightChild = None
    
    def getData(self):
        return self.data;
    
    def setData(self, data):
        self.data = data;
        
    def getLeft(self):
        return self.next;
    
    def setLeft(self, nextNode):
        self.next = nextNode;
        
    def getRight(self):
        return self.next;
    
    def setRight(self, nextNode):
        self.next = nextNode;